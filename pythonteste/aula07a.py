n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
s = n1 + n2
sub = n1 - n2
d = n1 / n2
di = n1 // n2
resdiv = n1 % n2
pot = n1 ** n2
print('A soma de {} e {} é: {}'.format(n1, n2, s))
print('A subtração de {} e {} é: {}'.format(n1, n2, sub))
print('A divisão de {} por {} é: {}'.format(n1, n2, d))
print('A divisão inteira de {} por {} é: {}'.format(n1, n2, di))
print('O resto da divisão de {} por {} é: {}'.format(n1, n2, resdiv))
print('{} elevado a {} é igual a: {}'.format(n1, n2, pot))


