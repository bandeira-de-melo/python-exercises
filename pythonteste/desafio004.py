elemento = input('Digite algo ')
print('Qual o tipo primitivo?', type(elemento))
print('É somente letras? ', elemento.isalpha())
print('É somente número? ', elemento.isnumeric())
print('É letras e/ou numeros?', elemento.isalnum())
print('É tudo maiúscula?', elemento.isupper())
print('É tudo minúscula?', elemento.islower())
print('Está capitalizada? ', elemento.istitle())





